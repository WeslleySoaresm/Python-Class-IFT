# convertendo int
numero = input("Digite um número: ")
numero_int = int(numero)
print(f"O número convertido para int é: {numero_int}")
print(f"O número {numero} foi convertido para int: {numero_int}")
# convertendo float
numero_flot = float(numero)
print(f"O número convertido para float é: {numero_flot}")
print(f"O número {numero} foi convertido para float: {numero_flot}")            
# convertendo str
numero_str = str(numero_int)
print(f"O número convertido para str é: {numero_str}")      
