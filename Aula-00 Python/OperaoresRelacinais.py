#Operadores Relacionais
a = 5
b = 10
c = 5

# Igualdade
print(a == b) # False
print(a == c) # True

# Desigualdade
print(a != b) # True
print(a != c) # False

# Maior que
print(a > b) # False
print(a > c) # False

# Menor que
print(a < b) # True
print(a < c) # False

# Maior ou igual
print(a >= b) # False
print(a >= c) # True

# Menor ou igual
print(a <= b) # True
print(a <= c) # True

